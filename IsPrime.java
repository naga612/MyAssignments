package week1.day1;

public class IsPrime {

	public static void main(String[] args) {
		int num = 8;
	for (int i = 2; i<=num; i++) {	
	if (num%i!=0) {
System.out.println("The number is Prime");
	break;
	} 
	else {
System.out.println("The number is NonPrime");
break;
	}	
	}
	
	}

}
